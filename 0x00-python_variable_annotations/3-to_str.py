#!/usr/bin/env python3
"""
Module for a function that returns the string representation of a float
with type annotations.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of a float number.

    Args:
        n (float): The float number to convert to a string.

    Returns:
        str: The string representation of the float number n.
    """
    return str(n)
